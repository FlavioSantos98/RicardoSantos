function overlaySwitch() {
    var overlayOpacity = document.getElementById('over').style.opacity;
    
    if (overlayOpacity == 0) {
        document.getElementById('over').style.opacity = "1";
        document.getElementById('over').style.pointerEvents = "auto";
        document.getElementById('atalhos').style.pointerEvents = "none";
        document.getElementById('gridGeral').style.pointerEvents = "none";
    } else {
        document.getElementById('over').style.opacity = "0";
        document.getElementById('over').style.pointerEvents = "none";
        document.getElementById('atalhos').style.pointerEvents = "auto";
        document.getElementById('gridGeral').style.pointerEvents = "auto";

        song.pause();

        for (let i = 1; i <= 15; i++) {
            idElement = "music" + i;
            document.getElementById(idElement).textContent = "";
        }
    }
}